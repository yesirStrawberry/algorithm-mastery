#include <stdio.h>
#include "str_kmp.h"

/**
 * @brief 테스트 시나리오를 실행하고 결과를 표준 포맷으로 출력하는 헬퍼 함수
 */
void run_test_case(int case_num, const char *description, const char *txt, const char *pat) {
    printf("========================================================================\n");
    printf("[시나리오 %d] %s\n", case_num, description);
    printf("- 텍스트 데이터: \"%s\"\n", txt);
    printf("- 탐색용 패턴  : \"%s\"\n", pat);
    printf("========================================================================\n");
    
    int result = kmp_match(txt, pat);
    
    printf("------------------------------------------------------------------------\n");
    if (result != -1) {
        printf("▶ [검색 성공] 패턴이 텍스트의 인덱스 [%d]번 위치에서 일치되었습니다.\n", result);
    } else {
        printf("◼ [검색 실패] 본문 내에 일치하는 패턴이 존재하지 않습니다. (반환값: %d)\n", result);
    }
    printf("========================================================================\n\n\n");
}

int main(void) {
    printf("########################################################################\n");
    printf("#                KMP 알고리즘 시나리오 기반 정밀 테스트                #\n");
    printf("########################################################################\n\n");

    /* Case 1: 기본 제공 테스트 케이스 (성공 패턴) */
    run_test_case(1, "기본 패턴 탐색 및 조기 매칭 검증", "ppapabab", "abab");

    /* Case 2: "ABABCABAD" 패턴 변형에 의한 연속 후퇴(3번 분기 폭발) 매칭 실패 시나리오
       본문의 마지막 'X'와 패턴의 마지막 'D'가 부딪치며 내부 스킵 테이블이 극단적으로 동작합니다. */
    run_test_case(2, "수정된 'ABABCABAD' 패턴의 기득권 붕괴 및 후퇴(3번 분기) 검증", 
                  "ABABCABAX", "ABABCABAD");

    /* Case 3: 텍스트 중간에 패턴과 유사한 접두사가 반복되다가 완전히 매칭되는 시나리오 */
    run_test_case(3, "중첩 접두사 구조를 가진 텍스트 내 매칭 성공 검증", 
                  "ABABCABABCABABA", "ABABCABAD");

    /* Case 4: 아예 일치하는 문자가 없는 극단적 예외 상황 */
    run_test_case(4, "완전 불일치 상태에서의 포인터 전진 스펙 검증", "XYZXYZXYZ", "abab");

    return 0;
}
